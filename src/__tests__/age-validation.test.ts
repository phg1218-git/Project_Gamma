import { describe, it, expect } from "vitest";
import { isAdult } from "@/lib/validations/profile";

/**
 * 만 나이 계산 테스트 (생일 경과 여부 반영)
 *
 * 기준일: 2026-03-22
 *
 * 핵심 케이스:
 *   - 2007-12-31 출생 → 생일이 아직 안 지남 → 만 18세 → 가입 실패 (버그 재현)
 *   - 2007-03-22 출생 → 오늘이 생일 당일 → 만 19세 → 가입 성공
 *   - 2007-03-21 출생 → 생일이 지났음 → 만 19세 → 가입 성공
 *   - 2007-03-23 출생 → 내일이 생일 → 만 18세 → 가입 실패
 *   - 2006-12-31 출생 → 만 19세 (생일 미경과) → 가입 성공
 */

const REF = new Date("2026-03-22"); // 기준일 고정

describe("isAdult — 만 나이 검증", () => {
  it("2007-12-31 출생자는 2026-03-22 기준 만 18세 → 가입 실패", () => {
    expect(isAdult(new Date("2007-12-31"), REF)).toBe(false);
  });

  it("2007-03-22 출생자는 2026-03-22 기준 생일 당일 → 만 19세 → 가입 성공", () => {
    expect(isAdult(new Date("2007-03-22"), REF)).toBe(true);
  });

  it("2007-03-21 출생자는 2026-03-22 기준 생일 경과 → 만 19세 → 가입 성공", () => {
    expect(isAdult(new Date("2007-03-21"), REF)).toBe(true);
  });

  it("2007-03-23 출생자는 2026-03-22 기준 생일 미경과 → 만 18세 → 가입 실패", () => {
    expect(isAdult(new Date("2007-03-23"), REF)).toBe(false);
  });

  it("2006-12-31 출생자는 2026-03-22 기준 만 19세 → 가입 성공", () => {
    expect(isAdult(new Date("2006-12-31"), REF)).toBe(true);
  });

  it("2006-03-23 출생자는 2026-03-22 기준 생일 미경과 → 만 19세 → 가입 성공", () => {
    expect(isAdult(new Date("2006-03-23"), REF)).toBe(true);
  });
});
